# ---------------------------------------------------------------------------
# Dice loss + metric
# ---------------------------------------------------------------------------

def dice_loss(pred: torch.Tensor, target: torch.Tensor, smooth: float = 1.0) -> torch.Tensor:
    """Soft Dice loss for binary segmentation.

    Args:
        pred: Predicted probabilities (B, 1, H, W) in [0, 1].
        target: Binary ground truth (B, 1, H, W) in {0, 1}.
        smooth: Laplace smoothing to avoid division by zero.

    Returns:
        Scalar Dice loss in [0, 1] (lower is better).
    """
    pred_flat = pred.view(pred.size(0), -1)
    tgt_flat = target.view(target.size(0), -1)
    intersection = (pred_flat * tgt_flat).sum(dim=1)
    dice = (2.0 * intersection + smooth) / (pred_flat.sum(dim=1) + tgt_flat.sum(dim=1) + smooth)
    return 1.0 - dice.mean()


def dice_score(pred: torch.Tensor, target: torch.Tensor, threshold: float = 0.5) -> float:
    """Hard Dice score (binarized prediction).

    Args:
        pred: Predicted probabilities (B, 1, H, W).
        target: Binary ground truth (B, 1, H, W).
        threshold: Binarization threshold.

    Returns:
        Mean Dice score in [0, 1] (higher is better).
    """
    pred_bin = (pred > threshold).float()
    pred_flat = pred_bin.view(pred_bin.size(0), -1)
    tgt_flat = target.view(target.size(0), -1)
    intersection = (pred_flat * tgt_flat).sum(dim=1)
    denom = pred_flat.sum(dim=1) + tgt_flat.sum(dim=1)
    # Avoid division by zero: empty prediction on empty target = 1
    score = torch.where(denom > 0, 2.0 * intersection / denom, torch.ones_like(intersection))
    return score.mean().item()


