def train_unet(
    train_dataset: SegmentationDataset,
    val_dataset: SegmentationDataset,
    output_dir: Path,
    epochs: int = 50,
    batch_size: int = 4,
    lr: float = 1e-4,
    device: torch.device = torch.device("cpu"),
    seed: int = 42,
) -> dict[str, list[float]]:
    """Train U-Net with combined Dice + BCE loss.

    Args:
        train_dataset: Training segmentation dataset.
        val_dataset: Validation segmentation dataset.
        output_dir: Directory to save checkpoints and logs.
        epochs: Number of training epochs.
        batch_size: Batch size.
        lr: Learning rate.
        device: Torch device.
        seed: Random seed.

    Returns:
        Dict with "train_dice", "val_dice", "train_loss", "val_loss" lists.
    """
    set_seed(seed)
    logger = get_logger("unet_trainer", log_dir=str(output_dir / "logs"))

    model = UNet(in_channels=1, base_features=32).to(device)
    optimizer = torch.optim.Adam(model.parameters(), lr=lr)
    scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=epochs)

    train_loader = DataLoader(
        train_dataset, batch_size=batch_size, shuffle=True,
        num_workers=2, drop_last=True, pin_memory=True,
    )
    val_loader = DataLoader(
        val_dataset, batch_size=batch_size, shuffle=False, num_workers=2,
    )

    history: dict[str, list[float]] = {
        "train_dice": [], "val_dice": [], "train_loss": [], "val_loss": [],
    }

    best_val_dice = 0.0

    for epoch in range(1, epochs + 1):
        # --- Train ---
        model.train()
        train_loss_sum = 0.0
        train_dice_sum = 0.0
        n_train = 0

        for batch in tqdm(train_loader, desc=f"Epoch {epoch}/{epochs} [train]", leave=False):
            images = batch["image"].to(device)
            masks = batch["mask"].to(device)

            optimizer.zero_grad()
            preds = model(images)
            loss = dice_loss(preds, masks) + F.binary_cross_entropy(preds, masks)
            loss.backward()
            optimizer.step()

            train_loss_sum += loss.item() * images.size(0)
            train_dice_sum += dice_score(preds.detach(), masks) * images.size(0)
            n_train += images.size(0)

        scheduler.step()

        # --- Validate ---
        model.eval()
        val_loss_sum = 0.0
        val_dice_sum = 0.0
        n_val = 0

        with torch.no_grad():
            for batch in tqdm(val_loader, desc=f"Epoch {epoch}/{epochs} [val]", leave=False):
                images = batch["image"].to(device)
                masks = batch["mask"].to(device)
                preds = model(images)
                loss = dice_loss(preds, masks) + F.binary_cross_entropy(preds, masks)
                val_loss_sum += loss.item() * images.size(0)
                val_dice_sum += dice_score(preds, masks) * images.size(0)
                n_val += images.size(0)

        train_loss = train_loss_sum / max(n_train, 1)
        val_loss = val_loss_sum / max(n_val, 1)
        train_d = train_dice_sum / max(n_train, 1)
        val_d = val_dice_sum / max(n_val, 1)

        history["train_loss"].append(train_loss)
        history["val_loss"].append(val_loss)
        history["train_dice"].append(train_d)
        history["val_dice"].append(val_d)

        logger.info(
            "Epoch %d/%d  train_loss=%.4f  val_loss=%.4f  train_dice=%.4f  val_dice=%.4f",
            epoch, epochs, train_loss, val_loss, train_d, val_d,
        )

        # Save best model
        if val_d > best_val_dice:
            best_val_dice = val_d
            torch.save(model.state_dict(), output_dir / "best_unet.pt")

    torch.save(model.state_dict(), output_dir / "final_unet.pt")
    with open(output_dir / "history.json", "w") as f:
        json.dump(history, f, indent=2)

    logger.info("Training complete. Best val Dice: %.4f", best_val_dice)
    return history


