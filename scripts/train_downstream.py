def main() -> None:
    parser = argparse.ArgumentParser(description="Downstream U-Net segmentation evaluation.")
    parser.add_argument(
        "--mode",
        type=str,
        required=True,
        choices=["real", "generate", "synthetic", "compare"],
        help=(
            "real: train on real images; "
            "generate: generate synthetic images from a checkpoint; "
            "synthetic: train on synthetic images; "
            "compare: load and print all result JSONs."
        ),
    )
    parser.add_argument("--data-dir", type=str, default="data/processed")
    parser.add_argument("--output-dir", type=str, default="outputs/downstream")
    parser.add_argument(
        "--checkpoint",
        type=str,
        default=None,
        help="CycleGAN checkpoint (required for --mode generate).",
    )
    parser.add_argument(
        "--synthetic-dir",
        type=str,
        default=None,
        help="Directory of synthetic .npy images (required for --mode synthetic).",
    )
    parser.add_argument("--epochs", type=int, default=50)
    parser.add_argument("--batch-size", type=int, default=4)
    parser.add_argument("--lr", type=float, default=1e-4)
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument(
        "--device",
        type=str,
        default="cuda" if torch.cuda.is_available() else "cpu",
    )
    parser.add_argument("--split", type=str, default="train", help="Split to generate from.")
    args = parser.parse_args()

    device = torch.device(args.device)
    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    # ------------------------------------------------------------------
    if args.mode == "generate":
        if args.checkpoint is None:
            parser.error("--checkpoint is required for --mode generate")
        generate_synthetic_images(
            checkpoint_path=args.checkpoint,
            processed_dir=args.data_dir,
            output_dir=str(output_dir / "images"),
            split=args.split,
            batch_size=args.batch_size,
            device=device,
        )
        return

    # ------------------------------------------------------------------
    if args.mode == "compare":
        results_dir = Path(args.output_dir)
        all_results = {}
        for result_file in sorted(results_dir.rglob("test_dice.json")):
            with open(result_file) as f:
                data = json.load(f)
            label = result_file.parent.name
            all_results[label] = data.get("test_dice", "N/A")

        if not all_results:
            print("No test_dice.json files found in", results_dir)
        else:
            print("\n=== Downstream Segmentation Results ===")
            for label, dice in sorted(all_results.items()):
                print(f"  {label:40s}: Dice = {dice:.4f}")
        return

    # ------------------------------------------------------------------
    # Build datasets
    synthetic_dir = args.synthetic_dir if args.mode == "synthetic" else None

    train_dataset = SegmentationDataset(
        processed_dir=args.data_dir,
        split="train",
        synthetic_image_dir=synthetic_dir,
        augment=True,
    )
    val_dataset = SegmentationDataset(
        processed_dir=args.data_dir,
        split="val",
        # Always validate on real images
        synthetic_image_dir=None,
        augment=False,
    )
    test_dataset = SegmentationDataset(
        processed_dir=args.data_dir,
        split="test",
        synthetic_image_dir=None,
        augment=False,
    )

    # Train
    print(f"\nTraining U-Net (mode={args.mode}, epochs={args.epochs})...")
    history = train_unet(
        train_dataset=train_dataset,
        val_dataset=val_dataset,
        output_dir=output_dir,
        epochs=args.epochs,
        batch_size=args.batch_size,
        lr=args.lr,
        device=device,
        seed=args.seed,
    )

    # Evaluate best checkpoint on test set
    best_ckpt = output_dir / "best_unet.pt"
    if best_ckpt.exists():
        test_dice = evaluate_unet(str(best_ckpt), test_dataset, device, args.batch_size)
    else:
        test_dice = evaluate_unet(str(output_dir / "final_unet.pt"), test_dataset, device, args.batch_size)

    print(f"\n=== Test Set Dice: {test_dice:.4f} ===")

    # Save result
    result = {
        "mode": args.mode,
        "test_dice": test_dice,
        "best_val_dice": max(history["val_dice"]) if history["val_dice"] else 0.0,
        "epochs": args.epochs,
        "synthetic_dir": str(synthetic_dir) if synthetic_dir else None,
    }
    with open(output_dir / "test_dice.json", "w") as f:
        json.dump(result, f, indent=2)
    print(f"Results saved to {output_dir / 'test_dice.json'}")


if __name__ == "__main__":
    main()
