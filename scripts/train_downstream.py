"""Train and evaluate a U-Net segmentation model on BraTS data.

Downstream experiment design (validates hypothesis H4):

  Condition A — "real": train on real pathological images + real masks (baseline).
  Condition B — "baseline_synthetic": train on synthetic images generated by
      baseline CycleGAN (G_BA(real_B)) paired with proxy masks from real
      pathological training slices.
  Condition C — "fb_synthetic": same as B but using FB-CycleGAN images.

All conditions are evaluated on held-out REAL BraTS test images + masks.
Higher Dice for FB-CycleGAN condition confirms that its outputs are more
structurally honest, producing better downstream segmentation models (H4).

Proxy mask assignment for synthetic images:
  Since G_BA(real_B) translates healthy images to synthetic pathological,
  there are no direct ground-truth masks. We assign proxy masks by randomly
  sampling a mask from the real pathological TRAINING split. This is a
  standard technique in domain-adaptation literature.

Usage:
    # Train on real images (baseline Dice)
    python scripts/train_downstream.py --mode real \
        --data-dir data/processed --output-dir outputs/downstream/real

    # Generate synthetic images first (requires trained generator checkpoint)
    python scripts/train_downstream.py --mode generate \
        --checkpoint outputs/checkpoints/baseline_cyclegan/final.pt \
        --data-dir data/processed \
        --output-dir outputs/downstream/baseline_synthetic/images

    # Train on synthetic images from baseline CycleGAN
    python scripts/train_downstream.py --mode synthetic \
        --synthetic-dir outputs/downstream/baseline_synthetic/images \
        --data-dir data/processed \
        --output-dir outputs/downstream/baseline_synthetic

    # Compare results
    python scripts/train_downstream.py --mode compare \
        --results-dir outputs/downstream
"""
