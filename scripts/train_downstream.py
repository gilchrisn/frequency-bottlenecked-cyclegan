# Evaluation
# ---------------------------------------------------------------------------

def evaluate_unet(
    checkpoint_path: str,
    test_dataset: SegmentationDataset,
    device: torch.device,
    batch_size: int = 4,
) -> float:
    """Evaluate U-Net Dice score on a test dataset.

    Args:
        checkpoint_path: Path to U-Net state_dict .pt file.
        test_dataset: Test segmentation dataset (real images + real masks).
        device: Torch device.
        batch_size: Batch size.

    Returns:
        Mean Dice score over the test set.
    """
    model = UNet(in_channels=1, base_features=32).to(device)
    model.load_state_dict(torch.load(checkpoint_path, map_location=device, weights_only=True))
    model.eval()

    loader = DataLoader(test_dataset, batch_size=batch_size, shuffle=False, num_workers=2)

    dice_sum = 0.0
    n = 0
    with torch.no_grad():
        for batch in loader:
            images = batch["image"].to(device)
            masks = batch["mask"].to(device)
            preds = model(images)
            dice_sum += dice_score(preds, masks) * images.size(0)
            n += images.size(0)

    return dice_sum / max(n, 1)


